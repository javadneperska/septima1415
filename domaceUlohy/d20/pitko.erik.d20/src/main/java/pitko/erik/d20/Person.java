package pitko.erik.d20;

public class Person {
	public String name;
	public String surname;
	public boolean sex;
	public int age;
	
	public Person(String name, String surname, int age, boolean sex) {
		super();
		this.name = name;
		this.surname = surname;
		this.age = age;
		this.sex = sex;
	}
	

}
